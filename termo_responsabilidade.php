<b><h3><font style="color: #00995D">TERMO DE RESPONSABILIDADE</font></h3></b><br><br>

1.	Os participantes da SUPER VOLTA UNIMED MTB devem estar cientes de que sua participação é por livre e espontânea vontade e de que trata-se de uma atividade que contém riscos de segurança e saúde;<br>
<br>
2.	Todos os participantes devem se encontrar em condições físicas e clínicas, e assim, aptos a participar do evento;<br>
<br>
3.	Todas as decisões tomadas pelos organizadores do evento relativas a possibilidade de não o terminar de maneira segura devem ser observadas e acatadas por todo e qualquer participante, assim como respeitar todas as regras impostas para o bom andamento do evento, sem exceções;<br>
<br>
4.	Além disso, todas as regras do Código de Trânsito Brasileiro deverão ser respeitadas e obedecidas nas vias públicas durante o evento;<br>
<br>
5.	Os ORGANIZADORES, PATROCINADORES e APOIADORES do evento não se responsabilizarão por quaisquer acidentes que o participante possa sofrer causando possível lesão física, tais como: quedas, contato com outros participantes, efeitos do clima, incluindo suas consequências, condições de tráfego e do circuito, além de outras que possam ter origem na sua falta de condicionamento físico, bem como, por eventuais quebras, furtos, roubos e avarias que possam ocorrer nos equipamentos durante o evento; <br>
<br>
6.	A idade mínima exigida para participar do passeio ciclístico é de 14 anos completos, até a data de inscrição;<br>
<br>
7.	Menores de 18 anos podem participar desde que sejam acompanhados pelos pais ou um responsável legal também inscrito.<br>
<br>
8.	Se o participante tiver entre 14 e 18 anos e não estiver acompanhado, será necessária autorização por escrito do pai ou responsável, que podem ser feitas das seguintes formas:<br>
<br>
•	Com firma reconhecida e acompanhada de cópia autenticada de um Documento de Identidade do pai ou responsável, que deverá ser apresentado pelo participante no ato da inscrição;<br>
<br>
•	Autorização de próprio punho feita no ato da inscrição, SOMENTE PELO PAI OU MÃE ou RESPONSÁVEL LEGAL, estando este (PAI, MÃE OU RESPONSÁVEL LEGAL) responsável fisicamente pelo menor. Neste caso, haverá a necessidade, de assinatura de duas testemunhas, que se encontrem no local, angariadas pelo participante;<br>
<br>
9.	Concordando com esses termos, o participante ainda permite aos organizadores e patrocinadores do evento que utilizem fotografias, filmagens, ou qualquer outra forma que mostra sua imagem e participação do evento;<br>
